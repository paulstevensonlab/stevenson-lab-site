// Helper functions for filtering and sorting

// Get all unique years
export const getPublicationYears = () => {
  const years = [...new Set(publications.map(pub => pub.year))];
  return years.sort((a, b) => {
    // Sort preprints first, then years in descending order
    if (a === "Preprint") return -1;
    if (b === "Preprint") return 1;
    return b - a;
  });
};

// Get publications by year
export const getPublicationsByYear = (year) => {
  return publications.filter(pub => pub.year === year);
};

// Get featured publications
export const getFeaturedPublications = () => {
  return publications.filter(pub => pub.featured);
};

// Get publications by type
export const getPublicationsByType = (type) => {
  return publications.filter(pub => pub.type === type);
};

// Get recent publications (last n publications)
export const getRecentPublications = (count = 5) => {
  return publications.slice(0, count);
};

// Search publications by title or authors
export const searchPublications = (query) => {
  const lowerQuery = query.toLowerCase();
  return publications.filter(pub => 
    pub.title.toLowerCase().includes(lowerQuery) ||
    pub.authors.toLowerCase().includes(lowerQuery) ||
    pub.journal.toLowerCase().includes(lowerQuery)
  );
};

